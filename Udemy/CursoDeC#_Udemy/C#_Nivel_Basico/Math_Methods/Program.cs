namespace Math_Methods
{
    class Program
    {
        public static void Main(string[] args)
        {
            Console.WriteLine("\nMaximum Method: " + Math.Max(15, 30));
            Console.WriteLine("Minimum Method: " + Math.Min(15, 30));
            Console.WriteLine("Sqrt Method: " + Math.Sqrt(100));
            Console.WriteLine("Abosulte Value Method: " + Math.Abs(-21.5));
            Console.WriteLine("Round Method: " + Math.Round(29.99));
            Console.WriteLine("Power Method: " + Math.Pow(2, 2) + "\n");
        }
    }
}
