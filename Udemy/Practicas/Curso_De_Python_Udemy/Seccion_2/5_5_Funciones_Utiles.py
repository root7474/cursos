def main():
    # Función sum()
    print(f"El valor redondeado de 3.4568 es: {round(3.4568)}") # Redondeamos el valor de 3.4568
    
    # Función abs()
    print(f"El valor absoluto de -56 es: {abs(-56)}") # Sacamos el valor absoluto de -56
    
    # Función min() y max()
    lista = [1, 9, 3, 8, 2, 6, 52, 75, 84, 21, 32]
    
    print(f"\nLista: {lista}")
    print(f"El valor mínimo de la lista es: {min(lista)}")
    print(f"El valor máximo de la lista es: {max(lista)}")
    
    # Función sorted()
    print(f"Lista ordenada: {sorted(lista)}")
    
    # Función sum()
    print(f"La suma total de la lista es: {sum(lista)}")
    

if __name__ == "__main__":
    main()
    