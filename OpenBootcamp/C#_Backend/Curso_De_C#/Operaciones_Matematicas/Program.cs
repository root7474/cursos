// Operaciones matemáticas
int num1 = 100;
Console.WriteLine($"Sqrt: {Math.Sqrt(num1)}"); // 10

float num2 = 9.99f;
Console.WriteLine($"Round: {Math.Round(num2)}"); // 10

float num3 = 9.01f;
Console.WriteLine($"Ceiling: {Math.Ceiling(num3)}"); // 10
Console.WriteLine($"Floor: {Math.Floor(num3)}"); // 9

Console.WriteLine($"Máximo: {Math.Max(num2, num3)}");
Console.WriteLine($"Mínimo: {Math.Min(num2, num3)}");
