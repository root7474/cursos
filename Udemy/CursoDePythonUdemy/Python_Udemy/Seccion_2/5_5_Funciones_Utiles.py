def main():
    # Función round()
    print(f"Valor redonado de 3.4568 es: {round(3.4568)}")
    
    # Función abs()
    print(f"Valor absoluto de -56 es: {abs(-56)}\n")
    
    # Función min() y max()
    lista = [1, 9, 3, 8, 2, 6, 52, 75, 84, 21, 32]
    
    print(f"Lista: {lista}")
    print(f"Valor mínimo de la lista: {min(lista)}")
    print(f"Valor máximo de la lista: {max(lista)}\n")
    
    # Función sorted()
    print(f"Lista ordenada: {sorted(lista)}")
    
    # Función sum()
    print(f"Suma total de la lista: {sum(lista)}")
    
if __name__ == "__main__":
    main()
